//Casting -> Processo de converter os tipos de dados de uma variável
/** Dois tipos
 * parseInt() -> converter para um inteiro
 * parseFloat() -> converter para um flutuante
 */
//Pararesolver um algoritmo eu tenho que seguir três passos
//1º Determinar os dados de entrada (variáveis e comando para receber valores)
let nota1, nota2, soma
//Comando para a entrada de dados
nota1 = parseInt(prompt("Digite sua nota"))
nota2 = parseInt(prompt("Digite sua nota"))

//2º Processamento -> trabalhar com as variáveis
soma = nota1 + nota2

//3º Saída do processamento. saída de dados
//Comandos para a saída de dados
document.write(soma)
